/* XIAO board I2C pins (D4=SDA, D5=SCL) differ per chip — from arduino-esp32 variants. */
#if defined(CONFIG_IDF_TARGET_ESP32C6)
#define OLED_SDA_GPIO   22     /* XIAO ESP32-C6 D4 */
#define OLED_SCL_GPIO   23     /* XIAO ESP32-C6 D5 */
#elif defined(CONFIG_IDF_TARGET_ESP32C5)
#define OLED_SDA_GPIO   23     /* XIAO ESP32-C5 D4 */
#define OLED_SCL_GPIO   24     /* XIAO ESP32-C5 D5 */
#elif defined(CONFIG_IDF_TARGET_ESP32C3)
#define OLED_SDA_GPIO   6      /* XIAO ESP32-C3 D4 */
#define OLED_SCL_GPIO   7      /* XIAO ESP32-C3 D5 */
#else
#define OLED_SDA_GPIO   5      /* XIAO ESP32-S3 D4 */
#define OLED_SCL_GPIO   6      /* XIAO ESP32-S3 D5 */
#endif
#define OLED_I2C_PORT   I2C_NUM_0
#define OLED_ADDR       0x3C
#define OLED_W          128
#define OLED_H          64

static const char *TAG = "oled";
static i2c_master_dev_handle_t s_dev;
static uint8_t s_fb[OLED_W * OLED_H / 8];   /* 1 bit/pixel, page-packed */

