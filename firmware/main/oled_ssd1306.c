/**
 * @file oled_ssd1306.c
 * @brief 128x64 SSD1306 I2C OLED renderer for RuView vitals on the Seeed
 *        XIAO Expansion Board. Standalone — does not touch the LVGL path.
 *
 * Wiring (XIAO ESP32-S3): SDA=D4=GPIO5, SCL=D5=GPIO6, addr 0x3C.
 * Polls edge_get_vitals() ~4 Hz and draws presence / breathing / heart /
 * persons / rssi. Safe to start always: if no panel ACKs, the task exits.
 */
#include "oled_ssd1306.h"
