static esp_err_t oled_cmd(uint8_t c)
{
    uint8_t b[2] = {0x00, c};
    return i2c_master_transmit(s_dev, b, 2, 100);
}

static void px(int x, int y, int on)
{
    if (x < 0 || x >= OLED_W || y < 0 || y >= OLED_H) return;
    uint32_t idx = (y >> 3) * OLED_W + x;
    uint8_t bit = 1 << (y & 7);
    if (on) s_fb[idx] |= bit; else s_fb[idx] &= ~bit;
}

/* Draw one char at top-left (x,y) with integer scale. */
static void draw_char(int x, int y, char c, int s)
{
    if (c < 0x20 || c > 0x7E) c = '?';
    const uint8_t *g = FONT5X7[c - 0x20];
    for (int col = 0; col < 5; col++) {
        uint8_t bits = g[col];
        for (int row = 0; row < 7; row++) {
            if (bits & (1 << row)) {
                for (int dx = 0; dx < s; dx++)
                    for (int dy = 0; dy < s; dy++)
                        px(x + col * s + dx, y + row * s + dy, 1);
            }
        }
    }
}

static void draw_str(int x, int y, const char *str, int s)
{
    for (; *str; str++) {
        draw_char(x, y, *str, s);
        x += 6 * s;   /* 5 px glyph + 1 px gap */
    }
}

static void oled_flush(void)
{
    oled_cmd(0x21); oled_cmd(0); oled_cmd(127);   /* column range */
    oled_cmd(0x22); oled_cmd(0); oled_cmd(7);     /* page range */
    for (int page = 0; page < 8; page++) {
        uint8_t buf[1 + OLED_W];
        buf[0] = 0x40;                            /* Co=0, D/C=1 (data) */
        memcpy(&buf[1], &s_fb[page * OLED_W], OLED_W);
        i2c_master_transmit(s_dev, buf, sizeof(buf), 100);
    }
}

static esp_err_t oled_panel_init(i2c_master_bus_handle_t bus)
{
    if (i2c_master_probe(bus, OLED_ADDR, 100) != ESP_OK) return ESP_ERR_NOT_FOUND;
    i2c_device_config_t dev_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address = OLED_ADDR,
        .scl_speed_hz = 400000,
    };
    ESP_ERROR_CHECK(i2c_master_bus_add_device(bus, &dev_cfg, &s_dev));

    static const uint8_t init_seq[] = {
        0xAE, 0x20, 0x00, 0xB0, 0xC8, 0x00, 0x10, 0x40, 0x81, 0x7F,
        0xA1, 0xA6, 0xA8, 0x3F, 0xA4, 0xD3, 0x00, 0xD5, 0x80, 0xD9,
        0xF1, 0xDA, 0x12, 0xDB, 0x40, 0x8D, 0x14, 0xAF,
    };
    for (size_t i = 0; i < sizeof(init_seq); i++) {
        esp_err_t e = oled_cmd(init_seq[i]);
        if (e != ESP_OK) return e;
    }
    memset(s_fb, 0, sizeof(s_fb));
    oled_flush();
    return ESP_OK;
}

static void oled_task(void *arg)
{
    char line[24];
    while (1) {
        edge_vitals_pkt_t v;
        bool ok = edge_get_vitals(&v);
        memset(s_fb, 0, sizeof(s_fb));

        if (!ok) {
            draw_str(0, 0, "RuView CSI", 1);
            draw_str(0, 28, "warming up...", 1);
            oled_flush();
            vTaskDelay(pdMS_TO_TICKS(300));
            continue;
        }

        bool presence = v.flags & 0x01;
        bool fall     = v.flags & 0x02;

        /* Header: title + RSSI */
        draw_str(0, 0, "RuView", 1);
        snprintf(line, sizeof(line), "%ddBm", v.rssi);
        draw_str(OLED_W - 6 * (int)strlen(line), 0, line, 1);

        /* Presence banner */
        draw_str(0, 10, presence ? "PRESENT" : "clear", 1);
        if (fall) draw_str(72, 10, "FALL!", 1);
        snprintf(line, sizeof(line), "x%u", v.n_persons);
        draw_str(OLED_W - 6 * (int)strlen(line), 10, line, 1);

        /* Big breathing + heart numbers (2x) */
        snprintf(line, sizeof(line), "%.0f", v.breathing_rate / 100.0);
        draw_str(0, 22, "BR", 1);
        draw_str(18, 20, line, 2);
        snprintf(line, sizeof(line), "%.0f", v.heartrate / 10000.0);
        draw_str(64, 22, "HR", 1);
        draw_str(82, 20, line, 2);

        /* Footer: precise values + motion */
        snprintf(line, sizeof(line), "b%.1f h%.1f m%.0f",
                 v.breathing_rate / 100.0, v.heartrate / 10000.0, v.motion_energy);
        draw_str(0, 40, line, 1);
        snprintf(line, sizeof(line), "score %.1f", v.presence_score);
        draw_str(0, 52, line, 1);

        oled_flush();
        vTaskDelay(pdMS_TO_TICKS(250));
    }
}

esp_err_t oled_task_start(void)
{
    i2c_master_bus_config_t bus_cfg = {
        .i2c_port = OLED_I2C_PORT,
        .sda_io_num = OLED_SDA_GPIO,
        .scl_io_num = OLED_SCL_GPIO,
        .clk_source = I2C_CLK_SRC_DEFAULT,
        .glitch_ignore_cnt = 7,
        .flags.enable_internal_pullup = true,
    };
    i2c_master_bus_handle_t bus;
    esp_err_t e = i2c_new_master_bus(&bus_cfg, &bus);
    if (e != ESP_OK) {
        ESP_LOGW(TAG, "I2C bus init failed: %s", esp_err_to_name(e));
        return ESP_OK;   /* non-fatal */
    }
    if (oled_panel_init(bus) != ESP_OK) {
        ESP_LOGW(TAG, "No SSD1306 at 0x%02X — OLED disabled", OLED_ADDR);
        return ESP_OK;   /* graceful: keep node headless */
    }
    xTaskCreatePinnedToCore(oled_task, "oled", 4096, NULL, 1, NULL, 0);
    ESP_LOGI(TAG, "SSD1306 OLED task started (SDA=%d SCL=%d)", OLED_SDA_GPIO, OLED_SCL_GPIO);
    return ESP_OK;
}
